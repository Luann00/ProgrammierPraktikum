package logic;

public class ElementTesting {
	
	private int groese;
	private int name;
	
	
	public ElementTesting(int groese, int name) {
		this.groese = groese;
		this.name = name;
	}


	public int getGroese() {
		return groese;
	}


	public void setGroese(int groese) {
		this.groese = groese;
	}


	public int getName() {
		return name;
	}


	public void setName(int name) {
		this.name = name;
	}
	
	
}



